library(hamcrest)


test.closure <- function() {

    definition <- function(x, value) 42L
    a <- args(definition)

    assertThat(environment(a), identicalTo(.GlobalEnv))
    assertThat(body(a), identicalTo(NULL))
    assertThat(formals(a), identicalTo(formals(definition)))
}

