library(hamcrest)

test.kMeans <- function() {

    x <- matrix(c(0.156207412152278, -0.0492830690901504, 0.262881457090765,  -0.131639581032605, -0.169640218177901,
           1.10924271207309, 1.34214312028469,  0.9930181222544, 1.06292254992274, 0.752444505513295, 0.0687438383594858,
           -0.112742585338682, -0.0400139229373574, -0.137509622466476,  -0.299573964903339, 0.82452114130542,
           1.78651530716763, 0.881468132749094,  1.50344133866565, 1.53498817305417), ncol=2)


    cl <- kmeans(x, 2)

    clusterNumbers <- unique(cl$cluster)
    assertThat(cl$cluster == clusterNumbers[1], equalTo(c(T,T,T,T,T,F,F,F,F,F)))
    assertThat(cl$cluster == clusterNumbers[2], equalTo(c(F,F,F,F,F,T,T,T,T,T)))
}